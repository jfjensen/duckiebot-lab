def _build_robot(args):
    """Construct the robot: real DuckiebotHUT, or the synthetic simulator."""
    if args.simulate:
        from ._sim import SimRobot
        return SimRobot(mode=args.sim_mode, imu_rate_hz=args.imu_rate)
    from pyhut import DuckiebotHUT
    return DuckiebotHUT(calibration=args.calibration)


def main(argv=None):
    p = argparse.ArgumentParser(
        prog="duckiebot-emi",
        description="Characterize motor EMI on the magnetometer and decide "
                    "whether the mag is usable as a live heading input during "
                    "the in-place lidar spin.")
    p.add_argument("--duties", default=",".join("%.2f" % d for d in DEFAULT_DUTIES),
                   help="comma-separated motor duties in (0,1]")
    p.add_argument("--pattern", choices=("spin", "straight"), default="spin",
                   help="drive pattern per level (default: in-place spin)")
    p.add_argument("--direction", choices=("cw", "ccw"), default="cw")
    p.add_argument("--dwell", type=float, default=3.0,
                   help="seconds of steady-state sampling per level")
    p.add_argument("--settle", type=float, default=1.0,
                   help="seconds to settle before sampling each level")
    p.add_argument("--baseline", type=float, default=3.0,
                   help="seconds of motors-off baseline per window")
    p.add_argument("--no-rebaseline", dest="rebaseline", action="store_false",
                   help="don't insert an off-window between every level")
    p.add_argument("--threshold", type=float, default=5.0,
                   help="worst-case heading-error budget in deg (default 5)")
    p.add_argument("--operating-duty", type=float, default=None,
                   help="the duty Stages 4/5 will spin at; pins the pass/fail")
    p.add_argument("--calibration", default=None,
                   help="path to a pyhut calibration JSON (recommended: match "
                        "Stages 4/5 by sampling through the calibrated view)")
    p.add_argument("--outdir", default="emi_runs",
                   help="directory for the saved run (default: emi_runs)")
    p.add_argument("--stem", default=None, help="output filename stem")
    p.add_argument("--plot", action="store_true",
                   help="also write a PNG (needs matplotlib)")
    p.add_argument("--simulate", action="store_true",
                   help="run against a synthetic robot (no hardware) for a demo")
    p.add_argument("--sim-mode", choices=("bench", "floor"), default="bench",
                   help="simulator mode (bench=held still, floor=really spins)")
    p.add_argument("--imu-rate", type=float, default=50.0,
                   help="simulator sampler rate (Hz)")
    args = p.parse_args(argv)

    duties = tuple(float(x) for x in args.duties.split(",") if x.strip())

    print("Motor-EMI characterization -- %s"
          % ("SIMULATED (%s)" % args.sim_mode if args.simulate else "LIVE hardware"))
    if not args.simulate:
        print("Ensure the robot is on a stand with the wheels free to spin "
              "(bench mode) for the cleanest result.")
    print("")

    robot = _build_robot(args)
    try:
        report = characterize(
            robot, duties=duties, pattern=args.pattern, direction=args.direction,
            settle_s=args.settle, dwell_s=args.dwell, baseline_s=args.baseline,
            rebaseline=args.rebaseline, threshold_deg=args.threshold,
            operating_duty=args.operating_duty,
            meta={"simulated": bool(args.simulate),
                  "sim_mode": args.sim_mode if args.simulate else None,
                  "calibration": args.calibration},
            on_progress=lambda m: print("  run: " + m))
    except KeyboardInterrupt:
        print("\nInterrupted -- stopping motors.")
        try:
            robot.drivetrain.stop()
        except Exception:
            pass
        return 2
    finally:
        try:
            robot.close()
        except Exception:
            pass

    print("")
    print(format_report_text(report))
    paths = save_report(report, outdir=args.outdir, stem=args.stem, plot=args.plot)
    print("Saved run:")
    for kind, path in paths.items():
        print("  %-4s %s" % (kind, path))
    return 0


if __name__ == "__main__":
    sys.exit(main())
