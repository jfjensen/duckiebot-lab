class EMIReport(object):
    """Everything a run produced: metadata, windows, levels, trend, verdict."""

    SCHEMA = "duckiebot_lab.motor_emi/1"

    def __init__(self, windows, levels, baseline_noise, verdict, meta):
        self.windows = windows
        self.levels = levels
        self.baseline_noise = baseline_noise
        self.verdict = verdict
        self.meta = meta
        slope, intercept = _linfit(
            [lv.duty for lv in levels],
            [lv.heading_err_worst for lv in levels])
        self.trend = {
            "heading_err_worst_vs_duty_slope_deg_per_duty": slope,
            "heading_err_worst_vs_duty_intercept_deg": intercept,
        }

    # -- serialization ------------------------------------------------------
    def to_dict(self):
        return {
            "schema": self.SCHEMA,
            "meta": self.meta,
            "verdict": {
                "status": self.verdict.status,
                "mag_live_usable": self.verdict.mag_live_usable,
                "max_safe_duty": self.verdict.max_safe_duty,
                "threshold_deg": self.verdict.threshold_deg,
                "operating_duty": self.verdict.operating_duty,
                "error_at_operating_deg": self.verdict.error_at_operating_deg,
                "reason": self.verdict.reason,
            },
            "baseline_noise": self.baseline_noise,
            "trend": self.trend,
            "levels": [self._level_dict(lv) for lv in self.levels],
            "windows": [self._window_dict(w) for w in self.windows],
        }

    @staticmethod
    def _level_dict(lv):
        return {
            "duty": lv.duty, "pattern": lv.pattern,
            "emi_vec_uT": list(lv.emi_vec),
            "emi_mag_uT": lv.emi_mag, "emi_horiz_uT": lv.emi_horiz,
            "heading_err_measured_deg": lv.heading_err_meas,
            "heading_err_worstcase_deg": lv.heading_err_worst,
            "field_mag_dev_pct": lv.field_mag_dev_pct,
            "gyro_rms_dps": lv.gyro_rms, "rotating": lv.rotating,
            "baseline_field_mag_uT": lv.baseline_field_mag,
            "baseline_horiz_uT": lv.baseline_horiz,
        }

    @staticmethod
    def _window_dict(w):
        return {
            "label": w.label, "pattern": w.pattern, "duty": w.duty,
            "t0": w.t0, "t1": w.t1, "n": w.n, "rate_hz": w.rate_hz,
            "mag_mean_uT": list(w.mag_mean) if w.mag_mean else None,
            "field_mag_mean_uT": w.field_mag_mean,
            "field_mag_std_uT": w.field_mag_std,
            "heading_mean_deg": w.heading_mean, "heading_std_deg": w.heading_std,
            "gyro_mean_dps": list(w.gyro_mean) if w.gyro_mean else None,
            "gyro_rms_dps": w.gyro_rms, "mag_ok": w.mag_ok,
        }


def save_report(report, outdir=".", stem=None, plot=False):
    """Write <stem>.json, <stem>.csv and <stem>.txt (and optionally .png).

    Returns the dict of written paths. JSON is the canonical record Stage 4
    reads; CSV is the sweep table; TXT is the human report.
    """
    if stem is None:
        stem = "motor_emi_" + time.strftime("%Y%m%d_%H%M%S")
    if not os.path.isdir(outdir):
        os.makedirs(outdir)
    base = os.path.join(outdir, stem)
    paths = {}

    with open(base + ".json", "w") as f:
        json.dump(report.to_dict(), f, indent=2)
    paths["json"] = base + ".json"

    with open(base + ".csv", "w", newline="") as f:
        wr = csv.writer(f)
        wr.writerow(["duty", "pattern", "emi_mag_uT", "emi_horiz_uT",
                     "heading_err_measured_deg", "heading_err_worstcase_deg",
                     "field_mag_dev_pct", "gyro_rms_dps", "rotating"])
        for lv in report.levels:
            wr.writerow(["%.3f" % lv.duty, lv.pattern,
                         "%.3f" % lv.emi_mag, "%.3f" % lv.emi_horiz,
                         "%.2f" % lv.heading_err_meas,
                         "%.2f" % lv.heading_err_worst,
                         "%.2f" % lv.field_mag_dev_pct,
                         ("%.2f" % lv.gyro_rms) if lv.gyro_rms is not None else "",
                         int(lv.rotating)])
    paths["csv"] = base + ".csv"

    with open(base + ".txt", "w") as f:
        f.write(format_report_text(report))
    paths["txt"] = base + ".txt"

    if plot:
        png = _try_plot(report, base + ".png")
        if png:
            paths["png"] = png

    return paths


def _try_plot(report, path):
    """Optional matplotlib plot; silently skipped if matplotlib isn't installed."""
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception:
        return None
    duties = [lv.duty for lv in report.levels]
    worst = [lv.heading_err_worst for lv in report.levels]
    meas = [lv.heading_err_meas for lv in report.levels]
    fig, ax = plt.subplots(figsize=(6, 4))
    ax.plot(duties, worst, "o-", label="worst-case heading error")
    ax.plot(duties, meas, "s--", label="measured heading error", alpha=0.7)
    ax.axhline(report.verdict.threshold_deg, color="r", ls=":",
               label="threshold %.0f deg" % report.verdict.threshold_deg)
    if report.verdict.max_safe_duty:
        ax.axvline(report.verdict.max_safe_duty, color="g", ls=":",
                   label="max safe duty %.2f" % report.verdict.max_safe_duty)
    ax.set_xlabel("motor duty")
    ax.set_ylabel("heading error (deg)")
    ax.set_title("Motor-EMI heading disturbance vs duty [%s]"
                 % report.verdict.status)
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    fig.savefig(path, dpi=120)
    plt.close(fig)
    return path


def format_report_text(report):
    """Human-readable report: verdict first, then the numbers behind it."""
    v = report.verdict
    bn = report.baseline_noise
    lines = []
    lines.append("=" * 70)
    lines.append("MOTOR-EMI CHARACTERIZATION  --  Duckiebot HUT v3.1")
    lines.append("=" * 70)
    lines.append("")
    lines.append("VERDICT:  %s   (mag live-usable during spin: %s)"
                 % (v.status, "YES" if v.mag_live_usable else "NO"))
    lines.append("")
    for chunk in _wrap(v.reason, 68):
        lines.append("  " + chunk)
    lines.append("")
    if v.max_safe_duty is not None:
        lines.append("  max safe in-place spin duty : %.2f" % v.max_safe_duty)
    if v.operating_duty is not None:
        lines.append("  operating duty (Stages 4/5) : %.2f (error %.1f deg)"
                     % (v.operating_duty, v.error_at_operating_deg))
    lines.append("  error threshold             : %.1f deg" % v.threshold_deg)
    lines.append("")
    lines.append("At-rest magnetometer baseline (motors off):")
    lines.append("  horizontal field |H|   : %.1f uT" % bn.get("horiz_uT", float("nan")))
    lines.append("  total field |B|        : %.1f uT (+/- %.2f)"
                 % (bn.get("field_mag_uT", float("nan")),
                    bn.get("field_mag_std_uT", float("nan"))))
    lines.append("  heading noise (std)    : %.2f deg over %d off-windows"
                 % (bn.get("heading_std_deg", float("nan")),
                    bn.get("n_off_windows", 0)))
    lines.append("")
    lines.append("Disturbance vs motor duty (pattern = in-place spin):")
    lines.append("  duty | EMI |B|  EMI |H| | head.err  worst | dB/B%  | gyroRMS")
    lines.append("  -----+----------------+-----------------+--------+--------")
    for lv in report.levels:
        g = ("%6.1f" % lv.gyro_rms) if lv.gyro_rms is not None else "   n/a"
        flag = " *" if lv.rotating else ""
        lines.append("  %.2f | %6.2f  %6.2f | %7.1f %7.1f | %6.1f | %s%s"
                     % (lv.duty, lv.emi_mag, lv.emi_horiz,
                        lv.heading_err_meas, lv.heading_err_worst,
                        lv.field_mag_dev_pct, g, flag))
    if any(lv.rotating for lv in report.levels):
        lines.append("")
        lines.append("  * gyro shows real chassis rotation in this window (floor")
        lines.append("    mode): the measured heading error there is contaminated")
        lines.append("    by genuine turning -- rely on EMI |B| deviation and the")
        lines.append("    worst-case column, which stay valid under rotation.")
    lines.append("")
    _slope = report.trend["heading_err_worst_vs_duty_slope_deg_per_duty"]
    if math.isnan(_slope):
        # Single duty (or degenerate x): a slope is undefined. If the duty was
        # repeated, the spread across repeats is the meaningful number -- a wide
        # spread at one duty is itself evidence the mag can't be trusted live.
        _errs = [lv.heading_err_worst for lv in report.levels
                 if not math.isnan(lv.heading_err_worst)]
        if len(_errs) >= 2:
            lines.append("Trend: n/a (single duty). Worst-case error across %d "
                         "repeats: min %.1f, max %.1f, spread %.1f deg."
                         % (len(_errs), min(_errs), max(_errs), max(_errs) - min(_errs)))
        else:
            lines.append("Trend: n/a (need >= 2 distinct duties).")
    else:
        lines.append("Trend: worst-case error ~= %.1f deg/duty * duty + %.1f deg"
                     % (_slope,
                        report.trend["heading_err_worst_vs_duty_intercept_deg"]))
    lines.append("")
    lines.append("Method: EMI vector = (field with motors on) - (bracketing")
    lines.append("motors-off baseline). Worst-case heading error = asin(|EMI_h|/|H|),")
    lines.append("the error that occurs when the EMI vector is perpendicular to the")
    lines.append("Earth field -- which the in-place spin sweeps through every turn.")
    lines.append("=" * 70)
    return "\n".join(lines) + "\n"


def _wrap(text, width):
    words = text.split()
    lines, cur = [], ""
    for w in words:
        if cur and len(cur) + 1 + len(w) > width:
            lines.append(cur)
            cur = w
        else:
            cur = (cur + " " + w) if cur else w
    if cur:
        lines.append(cur)
    return lines


