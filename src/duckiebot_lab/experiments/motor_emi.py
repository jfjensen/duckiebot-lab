"""
Motor-EMI characterization diagnostic (Stage 2b, app-side).

Question this answers
---------------------
The motor board (PCA9685 + TB6612 + the motor leads/coils) sits on the same HUT
as the AK8963 magnetometer. Current through the motor circuit makes a magnetic
field that the mag picks up, so *spinning the motors may corrupt the heading*.
Stages 4/5 do an in-place 360 deg spin to build a "lidar" scan and want to tag
each range with a heading. This experiment decides whether the magnetometer is
usable as a LIVE heading input during that spin, or only as a static
before/after reference.

The physics, and how we separate EMI from real rotation
-------------------------------------------------------
The mag measures the field in the *sensor* frame:

    measured(theta) = R(theta) . B_earth   +   b_emi

* ``R(theta) . B_earth`` is Earth's field seen at chassis heading ``theta``. Its
  magnitude is constant no matter how the robot is turned -- rotation only
  rotates the vector.
* ``b_emi`` is generated by motor current. The leads/coils are fixed relative to
  the mag, so for a *constant* motor state ``b_emi`` is a roughly constant vector
  in the sensor frame, independent of ``theta``. Its size grows with current,
  which grows with duty (and mechanical load).

Two consequences we exploit:

1. **Bench mode (chassis held still, wheels free on a stand).** ``theta`` is
   fixed, so ``measured_on(d) - measured_off = b_emi(d)`` *directly*. We recover
   the whole EMI vector per duty and read the induced heading error straight off
   it. This is the clean characterization and the default.

2. **Field-magnitude invariance (works even on the floor).** Real rotation
   preserves ``|measured|``; EMI does not (it adds a motor-fixed vector, moving
   the |B| off its rotation value). So a change in ``|B|`` that tracks duty is
   EMI, cleanly separated from an honest heading change. The gyro is the
   cross-check: near-zero gyro confirms we're in bench mode and the raw heading
   error is trustworthy; large gyro means the chassis is really turning and we
   lean on the |B| deviation instead.

From an EMI vector to the number Stages 4/5 care about
------------------------------------------------------
Heading is ``atan2(By, Bx)`` on the horizontal components (this mirrors pyhut's
``Magnetometer.heading_deg``). An EMI offset of horizontal magnitude ``|e_xy|``
against a horizontal Earth field of magnitude ``H`` tilts that angle. During a
full in-place spin the EMI vector sweeps through every direction relative to
``H``, so the worst case *will* occur; the conservative planning number is

    worst_heading_error = asin( |e_xy| / H )        (180 deg if |e_xy| >= H)

That worst-case error, compared against a threshold, drives the verdict.

Hardware notes carried from the project
---------------------------------------
* The left and right drive paths are electrically asymmetric (left motor is all
  PCA9685 channels; right is GPIO direction + PCA PWM), and an in-place spin runs
  one motor forward and one in reverse. Their EMI signatures can therefore
  differ, so "spin" is characterized as its own pattern rather than assumed equal
  to straight driving.
* pyhut's drivetrain has a 0.3 s watchdog that zeroes the wheels if no fresh
  target arrives, so every drive window re-asserts the target continuously.
* We sample through ``robot.sampler`` (the Stage 1 Sampler), which already
  carries *calibrated* data when the HUT was built with a calibration -- the same
  view Stages 4/5 consume -- so the measured heading error is what they'd see.

Everything here is standard-library and Python 3.6-safe: this runs on the stock
JetPack image on the robot. Plotting (matplotlib) is optional and off-robot.
"""

