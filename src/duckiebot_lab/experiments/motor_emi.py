# Sample collection
# ===========================================================================
class _Collector(object):
    """Sampler subscriber that buffers (t, mag, gyro) tuples thread-safely.

    Runs on the sampler's thread, so on_sample stays a cheap list append (the
    Sampler contract requires callbacks be non-blocking).
    """

    def __init__(self):
        import threading
        self._lock = threading.Lock()
        self._buf = []

    def on_sample(self, sample):
        with self._lock:
            self._buf.append((sample.t, sample.mag, sample.gyro))

    def drain(self):
        with self._lock:
            out = self._buf
            self._buf = []
        return out


def _pattern_targets(pattern, duty, direction):
    """Map (pattern, duty, direction) -> (left, right) wheel targets."""
    if pattern == "off" or duty == 0.0:
        return 0.0, 0.0
    if pattern == "straight":
        return duty, duty
    if pattern == "spin":
        return (duty, -duty) if direction == "cw" else (-duty, duty)
    raise ValueError("unknown pattern: %r" % (pattern,))


def _drive_window(drivetrain, left, right, settle_s, dwell_s, collector,
                  reassert_hz=10.0):
    """Assert a target, wait to settle, then collect steady-state samples.

    Re-asserts the target the whole time so pyhut's 0.3 s watchdog stays fed.
    Returns the list of samples captured during the dwell phase only.
    """
    period = 1.0 / reassert_hz
    # Settle: assert and discard transients.
    t_end = time.monotonic() + settle_s
    while time.monotonic() < t_end:
        drivetrain.set_target(left, right)
        time.sleep(period)
    # Dwell: keep asserting, keep the samples that arrive from now on.
    collector.drain()                    # discard settle-phase samples
    t_end = time.monotonic() + dwell_s
    while time.monotonic() < t_end:
        drivetrain.set_target(left, right)
        time.sleep(period)
    return collector.drain()


def _summarize(label, pattern, duty, samples):
    """Reduce raw (t, mag, gyro) samples to a WindowStats."""
    ts = [s[0] for s in samples]
    mags = [s[1] for s in samples if s[1] is not None]
    gyros = [s[2] for s in samples if s[2] is not None]

    t0 = ts[0] if ts else float("nan")
    t1 = ts[-1] if ts else float("nan")
    span = (t1 - t0) if (ts and t1 > t0) else 0.0
    rate = (len(ts) / span) if span > 0 else float("nan")

    if mags:
        mx = _mean([m[0] for m in mags])
        my = _mean([m[1] for m in mags])
        mz = _mean([m[2] for m in mags])
        mag_mean = (mx, my, mz)
        fmags = [math.sqrt(m[0] ** 2 + m[1] ** 2 + m[2] ** 2) for m in mags]
        headings = [_heading_deg(m[0], m[1]) for m in mags]
        field_mag_mean = _mean(fmags)
        field_mag_std = _std(fmags)
        heading_mean = _circular_mean_deg(headings)
        heading_std = _circular_std_deg(headings)
        mag_ok = True
    else:
        mag_mean = None
        field_mag_mean = field_mag_std = float("nan")
        heading_mean = heading_std = float("nan")
        mag_ok = False

    if gyros:
        gx = _mean([g[0] for g in gyros])
        gy = _mean([g[1] for g in gyros])
        gz = _mean([g[2] for g in gyros])
        gyro_mean = (gx, gy, gz)
        gyro_rms = math.sqrt(
            _mean([g[0] ** 2 + g[1] ** 2 + g[2] ** 2 for g in gyros]))
    else:
        gyro_mean = None
        gyro_rms = None

    return WindowStats(
        label=label, pattern=pattern, duty=duty,
        t0=t0, t1=t1, n=len(ts), rate_hz=rate,
        mag_mean=mag_mean,
        field_mag_mean=field_mag_mean, field_mag_std=field_mag_std,
        heading_mean=heading_mean, heading_std=heading_std,
        gyro_mean=gyro_mean, gyro_rms=gyro_rms, mag_ok=mag_ok,
    )


