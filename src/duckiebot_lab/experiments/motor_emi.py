# One drive window's aggregated sensor statistics.
WindowStats = namedtuple("WindowStats", [
    "label", "pattern", "duty",
    "t0", "t1", "n", "rate_hz",
    "mag_mean",                 # (x, y, z) uT, or None if no mag samples
    "field_mag_mean", "field_mag_std",
    "heading_mean", "heading_std",
    "gyro_mean",                # (x, y, z) deg/s, or None
    "gyro_rms",                 # scalar deg/s, or None
    "mag_ok",
])

# Per-duty EMI result, referenced to a local motors-off baseline.
LevelResult = namedtuple("LevelResult", [
    "duty", "pattern",
    "emi_vec",                  # (x, y, z) uT: measured_on - baseline
    "emi_mag", "emi_horiz",
    "heading_err_meas",         # measured heading shift vs baseline (deg)
    "heading_err_worst",        # worst-case tilt from emi_horiz (deg)
    "field_mag_dev_pct",        # |B| change vs baseline, percent
    "gyro_rms", "rotating",     # rotation cross-check
    "baseline_field_mag", "baseline_horiz",
])

# The verdict Stage 4 keys off.
Verdict = namedtuple("Verdict", [
    "status",                   # LIVE_OK | MARGINAL | STATIC_ONLY
    "mag_live_usable",          # bool: fuse mag during the spin?
    "max_safe_duty",            # float or None: highest duty keeping error <= thr
    "threshold_deg",
    "operating_duty",           # float or None: the duty Stages 4/5 will use
    "error_at_operating_deg",   # float or None
    "reason",
])


