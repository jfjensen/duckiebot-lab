def _baseline_for(windows, i):
    """Average the motors-off windows bracketing active window index i.

    Returns (mag_vec, field_mag, horiz, heading) from the nearest off-window
    before and after i (whichever exist).
    """
    offs = []
    # nearest off before
    for j in range(i - 1, -1, -1):
        if windows[j].pattern == "off" and windows[j].mag_ok:
            offs.append(windows[j])
            break
    # nearest off after
    for j in range(i + 1, len(windows)):
        if windows[j].pattern == "off" and windows[j].mag_ok:
            offs.append(windows[j])
            break
    if not offs:
        return None
    vx = _mean([w.mag_mean[0] for w in offs])
    vy = _mean([w.mag_mean[1] for w in offs])
    vz = _mean([w.mag_mean[2] for w in offs])
    fm = _mean([w.field_mag_mean for w in offs])
    horiz = math.sqrt(vx ** 2 + vy ** 2)
    heading = _heading_deg(vx, vy)
    return (vx, vy, vz), fm, horiz, heading


def analyze(windows, rotating_gyro_dps=5.0):
    """Turn WindowStats into per-duty LevelResults plus baseline noise figures.

    Returns (levels, baseline_noise) where baseline_noise is a dict with the
    magnetometer's at-rest heading and |B| scatter -- the floor below which an
    EMI effect is indistinguishable from sensor noise.
    """
    levels = []
    for i, w in enumerate(windows):
        if w.pattern == "off" or not w.mag_ok:
            continue
        base = _baseline_for(windows, i)
        if base is None:
            continue
        (bx, by, bz), base_fm, base_horiz, base_heading = base

        emi_vec = (w.mag_mean[0] - bx, w.mag_mean[1] - by, w.mag_mean[2] - bz)
        emi_mag = math.sqrt(sum(c ** 2 for c in emi_vec))
        emi_horiz = math.sqrt(emi_vec[0] ** 2 + emi_vec[1] ** 2)

        heading_err_meas = abs(_ang_diff_deg(w.heading_mean, base_heading))
        heading_err_worst = _worst_heading_error_deg(emi_horiz, base_horiz)
        field_dev_pct = (100.0 * (w.field_mag_mean - base_fm) / base_fm
                         if base_fm else float("nan"))
        rotating = (w.gyro_rms is not None and w.gyro_rms > rotating_gyro_dps)

        levels.append(LevelResult(
            duty=w.duty, pattern=w.pattern,
            emi_vec=emi_vec, emi_mag=emi_mag, emi_horiz=emi_horiz,
            heading_err_meas=heading_err_meas,
            heading_err_worst=heading_err_worst,
            field_mag_dev_pct=field_dev_pct,
            gyro_rms=w.gyro_rms, rotating=rotating,
            baseline_field_mag=base_fm, baseline_horiz=base_horiz,
        ))

    offs = [w for w in windows if w.pattern == "off" and w.mag_ok]
    baseline_noise = {
        "heading_std_deg": _mean([w.heading_std for w in offs]) if offs else float("nan"),
        "field_mag_std_uT": _mean([w.field_mag_std for w in offs]) if offs else float("nan"),
        "field_mag_uT": _mean([w.field_mag_mean for w in offs]) if offs else float("nan"),
        "horiz_uT": (math.sqrt(offs[0].mag_mean[0] ** 2 + offs[0].mag_mean[1] ** 2)
                     if offs else float("nan")),
        "n_off_windows": len(offs),
    }
    return levels, baseline_noise


def _worst_by_duty(levels):
    """Collapse repeated duties to their WORST (max) error.

    When a duty is measured several times (e.g. --duties 0.5,0.5,0.5), the
    conservative planning number is the worst observed worst-case, not an
    arbitrary one of the repeats. Interpolating over the raw list would let
    adjacent duplicates hide the bad samples; collapsing to the max first makes
    the verdict see the true envelope.
    """
    worst = {}
    for lv in levels:
        if math.isnan(lv.heading_err_worst):
            continue
        if lv.duty not in worst or lv.heading_err_worst > worst[lv.duty]:
            worst[lv.duty] = lv.heading_err_worst
    return sorted(worst.items())


def _interp_error_at(levels, duty):
    """Linear-interpolate worst-case heading error at an arbitrary duty.

    Repeated duties collapse to their worst error first (see _worst_by_duty).
    """
    pts = _worst_by_duty(levels)
    if not pts:
        return float("nan")
    if duty <= pts[0][0]:
        return pts[0][1]
    if duty >= pts[-1][0]:
        return pts[-1][1]
    for (d0, e0), (d1, e1) in zip(pts, pts[1:]):
        if d0 <= duty <= d1:
            f = (duty - d0) / (d1 - d0) if d1 > d0 else 0.0
            return e0 + f * (e1 - e0)
    return pts[-1][1]


def _max_safe_duty(levels, threshold_deg):
    """Highest duty (interpolated crossing) whose worst error stays <= threshold.

    Repeated duties collapse to their worst error first, so a single bad repeat
    at a duty disqualifies that duty.
    """
    pts = _worst_by_duty(levels)
    if not pts:
        return None
    if pts[0][1] > threshold_deg:
        return None                      # over threshold even at the lowest duty
    safe = pts[0][0]
    for (d0, e0), (d1, e1) in zip(pts, pts[1:]):
        if e1 <= threshold_deg:
            safe = d1
        else:                            # threshold crossed in (d0, d1): interpolate
            if e1 > e0:
                f = (threshold_deg - e0) / (e1 - e0)
                safe = d0 + f * (d1 - d0)
            break
    return safe


def decide_verdict(levels, threshold_deg=5.0, operating_duty=None,
                   baseline_noise=None):
    """Decide LIVE_OK / MARGINAL / STATIC_ONLY from the per-duty errors.

    * LIVE_OK    -- worst-case error stays under threshold across every duty
                    tested (or at least up to the operating duty).
    * MARGINAL   -- under threshold at low duty, over it at higher duty; the mag
                    is live-usable only if the spin is kept at or below
                    ``max_safe_duty``.
    * STATIC_ONLY-- over threshold even at the lowest duty tested: use the mag as
                    a static before/after reference, hold last-known heading
                    through the spin, re-anchor after.

    ``operating_duty`` is the duty Stages 4/5 will actually spin at. When given,
    the pass/fail hinges on the error there; otherwise it hinges on the full
    tested range.
    """
    tested = sorted(lv.duty for lv in levels
                    if not math.isnan(lv.heading_err_worst))
    if not tested:
        return Verdict("STATIC_ONLY", False, None, threshold_deg,
                       operating_duty, None,
                       "No usable magnetometer levels were measured.")

    # If the chassis was really turning during any level (floor mode), the EMI
    # vector recovered as (on - off) is contaminated by genuine rotation, so the
    # numbers below are lower-confidence. Bench mode (robot on a stand) is the
    # clean characterization; flag this so Stage 4 doesn't over-trust the call.
    rotated = any(lv.rotating for lv in levels)
    caution = (" NOTE: the gyro shows real chassis rotation during the sweep, "
               "so this was not a clean bench run -- the EMI figures are "
               "approximate; re-run on a stand (wheels free) to confirm."
               if rotated else "")

    max_safe = _max_safe_duty(levels, threshold_deg)
    err_at_lowest = _interp_error_at(levels, tested[0])
    err_at_highest = _interp_error_at(levels, tested[-1])

    err_op = _interp_error_at(levels, operating_duty) if operating_duty is not None else None

    # Optional noise gate: an effect below the at-rest heading scatter isn't
    # really distinguishable from noise, so don't fail a robot on it.
    noise_deg = None
    if baseline_noise is not None:
        noise_deg = baseline_noise.get("heading_std_deg")

    if operating_duty is not None:
        usable = (err_op <= threshold_deg)
        if usable:
            status = "LIVE_OK"
            reason = ("Worst-case heading error at the operating duty %.2f is "
                      "%.1f deg, within the %.1f deg budget -- fuse the mag live."
                      % (operating_duty, err_op, threshold_deg))
        elif max_safe is not None and max_safe > 0:
            status = "MARGINAL"
            reason = ("Worst-case error at the operating duty %.2f is %.1f deg "
                      "(> %.1f deg), but stays within budget up to duty %.2f. "
                      "Usable live only if the spin is kept at/under that duty."
                      % (operating_duty, err_op, threshold_deg, max_safe))
        else:
            status = "STATIC_ONLY"
            reason = ("Worst-case error at the operating duty %.2f is %.1f deg, "
                      "over the %.1f deg budget even at the lowest duty tested -- "
                      "use the mag as a static reference only."
                      % (operating_duty, err_op, threshold_deg))
        return Verdict(status, usable, max_safe, threshold_deg,
                       operating_duty, err_op, reason + caution)

    # No operating duty pinned: judge across the tested range.
    if err_at_highest <= threshold_deg:
        status, usable = "LIVE_OK", True
        reason = ("Worst-case heading error stays at or below %.1f deg across "
                  "the whole tested range (max %.1f deg at duty %.2f) -- the mag "
                  "is usable as a live input during the spin."
                  % (threshold_deg, err_at_highest, tested[-1]))
    elif max_safe is not None and max_safe > tested[0]:
        status, usable = "MARGINAL", True
        reason = ("Worst-case error crosses %.1f deg around duty %.2f (%.1f deg "
                  "at the top duty %.2f). The mag is live-usable only if the "
                  "in-place spin is kept at/under duty %.2f."
                  % (threshold_deg, max_safe, err_at_highest, tested[-1], max_safe))
    else:
        status, usable = "STATIC_ONLY", False
        reason = ("Worst-case error is %.1f deg already at the lowest duty "
                  "tested (%.2f) and worse above -- the mag is NOT usable live "
                  "during the spin. Use it as a static before/after reference: "
                  "hold the last good heading through the turn and re-anchor after."
                  % (err_at_lowest, tested[0]))

    # Noise gate (conservative, one-directional): only *forgive* an over-budget
    # result when the disturbance is genuinely small in ABSOLUTE terms AND the
    # at-rest noise floor is itself tight. A large noise floor must never be a
    # licence to fuse -- a heading sensor that is already noisy at rest is worse,
    # not safer. So the gate requires the noise floor to be well under threshold
    # before it can soften anything, and it never overrides a real over-budget
    # error that exceeds the threshold on its own.
    if noise_deg is not None and not math.isnan(noise_deg) \
            and noise_deg <= 0.5 * threshold_deg \
            and err_at_highest <= threshold_deg \
            and err_at_highest <= 3.0 * noise_deg \
            and status != "LIVE_OK":
        # Effect is both under budget and within a few multiples of a *tight*
        # noise floor: not clearly distinguishable from noise, so don't fail on it.
        status, usable = "LIVE_OK", True
        reason = ("Worst-case EMI heading error (%.1f deg) is under the %.1f deg "
                  "budget and within ~3x a tight at-rest noise floor (%.1f deg), "
                  "so it is not clearly distinguishable from sensor noise -- "
                  "treat the mag as usable live." % (err_at_highest, threshold_deg, noise_deg))
    elif noise_deg is not None and not math.isnan(noise_deg) \
            and noise_deg > 0.5 * threshold_deg:
        # Noise floor is a large fraction of (or exceeds) the budget: the mag is
        # a coarse heading source even at rest. Flag it regardless of verdict.
        caution += (" NOTE: at-rest heading noise (%.1f deg) is large relative to "
                    "the %.1f deg budget -- this magnetometer is a coarse heading "
                    "reference even before EMI; do not treat it as precise."
                    % (noise_deg, threshold_deg))

    return Verdict(status, usable, max_safe, threshold_deg, None, None,
                   reason + caution)


