# ===========================================================================
# Small math helpers (std-lib only)
# ===========================================================================
def _mean(xs):
    return statistics.mean(xs) if xs else float("nan")


def _std(xs):
    return statistics.pstdev(xs) if len(xs) > 1 else 0.0


def _heading_deg(mx, my):
    """Compass heading in [0, 360) from horizontal field, matching pyhut."""
    return (math.degrees(math.atan2(my, mx)) + 360.0) % 360.0


def _ang_diff_deg(a, b):
    """Signed smallest difference a - b, in (-180, 180]."""
    return (a - b + 180.0) % 360.0 - 180.0


def _circular_mean_deg(angles):
    """Mean of headings, robust to the 0/360 wrap."""
    if not angles:
        return float("nan")
    s = sum(math.sin(math.radians(a)) for a in angles)
    c = sum(math.cos(math.radians(a)) for a in angles)
    return (math.degrees(math.atan2(s, c)) + 360.0) % 360.0


def _circular_std_deg(angles):
    """RMS of angular residuals about the circular mean."""
    if len(angles) < 2:
        return 0.0
    m = _circular_mean_deg(angles)
    res = [_ang_diff_deg(a, m) for a in angles]
    return math.sqrt(sum(r * r for r in res) / len(res))


def _worst_heading_error_deg(emi_horiz, baseline_horiz):
    """Worst-case heading tilt from a horizontal EMI offset (see module docs)."""
    if baseline_horiz <= 0.0:
        return float("nan")
    ratio = emi_horiz / baseline_horiz
    if ratio >= 1.0:
        return 180.0
    return math.degrees(math.asin(ratio))


def _linfit(xs, ys):
    """Ordinary least-squares slope/intercept; (nan, nan) if degenerate."""
    pts = [(x, y) for x, y in zip(xs, ys)
           if not (math.isnan(x) or math.isnan(y))]
    n = len(pts)
    if n < 2:
        return float("nan"), float("nan")
    mx = sum(x for x, _ in pts) / n
    my = sum(y for _, y in pts) / n
    den = sum((x - mx) ** 2 for x, _ in pts)
    if den == 0.0:
        return float("nan"), my
    slope = sum((x - mx) * (y - my) for x, y in pts) / den
    return slope, my - slope * mx


