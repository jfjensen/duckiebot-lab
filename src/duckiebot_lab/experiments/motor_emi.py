# High-level convenience
# ===========================================================================
def characterize(robot, duties=DEFAULT_DUTIES, pattern="spin", direction="cw",
                 settle_s=1.0, dwell_s=3.0, baseline_s=3.0, rebaseline=True,
                 threshold_deg=5.0, operating_duty=None, rotating_gyro_dps=5.0,
                 meta=None, on_progress=None):
    """Run + analyze + decide in one call. Returns an EMIReport."""
    windows = run_experiment(
        robot, duties=duties, pattern=pattern, direction=direction,
        settle_s=settle_s, dwell_s=dwell_s, baseline_s=baseline_s,
        rebaseline=rebaseline, on_progress=on_progress)
    levels, baseline_noise = analyze(windows, rotating_gyro_dps=rotating_gyro_dps)
    verdict = decide_verdict(levels, threshold_deg=threshold_deg,
                             operating_duty=operating_duty,
                             baseline_noise=baseline_noise)
    full_meta = {
        "created": time.strftime("%Y-%m-%dT%H:%M:%S"),
        "pattern": pattern, "direction": direction,
        "duties": list(duties),
        "settle_s": settle_s, "dwell_s": dwell_s, "baseline_s": baseline_s,
        "rebaseline": rebaseline, "rotating_gyro_dps": rotating_gyro_dps,
    }
    if meta:
        full_meta.update(meta)
    return EMIReport(windows, levels, baseline_noise, verdict, full_meta)


